# Print a greeting
print("Hello, World!")

# Basic arithmetic
x = 10
y = 5
result = x + y
print(f"The sum of {x} and {y} is {result}")

# If-else example
age = 18
if age >= 18:
    print("You are an adult.")
else:
    print("You are a minor.")

# For loop example
fruits = ["apple", "banana", "cherry"]
for fruit in fruits:
    print(fruit)

# While loop example
count = 1
while count <= 5:
    print(count)
    count += 1
